    {
        //
        $user=User::findOrFail( $request->user_id );
        if($user){
            if (! isset($request->current_password) || ! Hash::check($request->current_password, $user->password)) {
                return ['error' =>true, "msg"=> "The provided password does not match your current password.","type" => "INVALID_CREDENTIAL"];
            }else{
    
                $user->forceFill([
                    'password' => Hash::make($request->new_password),
                ])->save();
                return ["success" => true, "msg" => "Password updated successfully"];
            }
        }else{
            return ['error' =>true, "msg"=> "user not found","type" => "INVALID_CREDENTIAL"];
        }

        
    }

