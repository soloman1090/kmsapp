                        <span>ABOUT OUR STRATEGY</span>
                        <h1>Market dislocation creates opportunity</h1>
                        <p>The opportunities created by the Great Deleveraging are unique in that nearly every borrower and every asset (regardless of credit quality) are impacted, hence the name “Great.” No matter who you are, if you were active in business over the past several years then you were inevitably borrowing to some extent. And if you were borrowing at all, then you were borrowing at low rates and relatively high asset values.</p>
                        <p>Now that the environment has shifted, regardless of the quality of the underlying asset, as loans mature and come due, there will be a gap created during the refinancing period where new equity capital must come in to pay down the overall size of the loan.
                        </p>
                        <h2>Funding the gap</h2>
                        <p>Our strategy is to focus on bridging the funding gap and providing rescue capital to borrowers in the midst of the liquidity crunch. By lending into the gap, we are able to invest at a healthy margin of safety, concentrating on high-quality assets with creditworthy borrowers—those who are experiencing circumstantial liquidity needs as a result of interest rates rising so rapidly through 2022 and 2023.</p>
                        <p>In these instances, the underlying assets themselves are typically unaffected by the financial turbulence happening in capital markets. Most frequently, the borrower is in the middle of a business plan to enhance the value of the property, such as new construction, renovations, or lease-up, and simply needs more time to reach stabilization and be ready for long-term, fixed-rate debt.</p>
                        
                        <p>EXAMPLES OF THIS KIND OF ACTIVITY INCLUDE:</p>
                        <ul>
                            <li>Originating and structuring real estate loans, including senior mortgage loans and subordinated mortgage loans</li>
                            <li>Providing mezzanine financing in the form of preferred equity, B-notes, or second trusts</li>
                            <li>Sizing the mezzanine or preferred loans to a GSE (e.g. Fannie Mae or Freddie Mac) exit</li>
                            <li>Financing residential construction and development</li>
                            <li>Acquiring subordinate notes and high-yield investments in the asset backed securities market,—single family rental portfolios, in particular</li>
                        </ul>
                        <br>
