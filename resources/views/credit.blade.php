                               <h1>What is private credit?</h1>
                               <p>Private credit (or private lending) is an asset class that consists generally of loans, fixed-income, or other structured investments that aim to deliver higher yields with lower overall risk when compared to equity investments. In other words, investors in private credit are lending money to borrowers in exchange for a fixed rate of return (often captured in the form of an interest rate or preferred return) but typically do not have any equity ownership or upside participation. Similar to other private market assets, private credit differs from publicly traded credit or fixed-income investments, such as bonds or asset-backed securities, because it is illiquid and consequently aims to deliver a higher relative return.</p>
                               <h1>The profitability of preparation</h1>
                               <p>As a result of the Fed's extraordinary actions, today it's more expensive to borrow money for 30 days than for 30 years. This is an unnatural state of affairs that we believe is creating a once-in-a-decade liquidity crunch that we're calling The Great Deleveraging.</p>
                               <p>During this period, individuals and companies seeking to borrow money, especially in the near term, will be forced to do so at significantly more favorable terms for investors. Higher interest rates will generally mean that borrowers will borrow at much lower leverage (which means lower risk). Virtually any loan maturing in 2023 will require a paydown, and for new loans the gap between the expected and actual proceeds will likely require the use of “bridge” or “mezzanine” financing.</p>
